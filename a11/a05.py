import math
a12 = '|{:<30}|'.format('left align')
print(a12)
a12 = '|{:>30}|'.format('right align')
print(a12)
a12 = '|{:^30}|'.format('center')
print(a12)
a12 = '|{:-^30}|'.format('center')
print(a12)
a12 = '{0:b} {0:o} {0:d} {0:x} {0:X}'.format(1000)
print(a12)
a12 = '{0} {0:f}'.format(math.pi)
print(a12)
a12 = '{:%}'.format(0.045)
print(a12)
a12 = '{:,}'.format(1000000000000000000)
print(a12)
a12 = '{:4.2f} {:2.2%}'.format(math.pi, 0.045)
print(a12)

from datetime import datetime
now = datetime.now()
a12 = 'Today is {:%Y-%m-%d}'.format(now)
print(a12)
a12 = 'Currnet time is {:%H:%M:%S}'.format(now)
print(a12)